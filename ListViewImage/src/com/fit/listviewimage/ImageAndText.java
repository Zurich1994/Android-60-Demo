package com.fit.listviewimage;

public class ImageAndText {

	private String imageUrl;
	private String text;
	public ImageAndText(String imageUrl, String text) {
		super();
		this.imageUrl = imageUrl;
		this.text = text;
	}
	
	
	public String getImageUrl() {
		return imageUrl;
	}
	public String getText() {
		return text;
	}
	
	
	
}
